    <script>  
        function toggleAllDoctors() {  
            const hiddenCards = document.querySelectorAll('.doctor-card.hidden');  
            const seeAllBtn = document.querySelector('.see-all');  
              
            hiddenCards.forEach(card => {  
                card.classList.toggle('show');  
            });  
              
            if (seeAllBtn.textContent === 'See All') {  
                seeAllBtn.textContent = 'Show Less';  
            } else {  
                seeAllBtn.textContent = 'See All';  
            }  
        }  

        function toggleDoctors(card) {  
            const names = card.querySelector('.doctor-names');  
            if (names) {  
                names.classList.toggle('show');  
                // Ensure initial state is hidden by removing 'show' if present on load  
                if (!names.classList.contains('hidden')) {  
                    names.classList.add('hidden');  
                }  
            }  
        }  

        // Ensure all doctor names are hidden on page load  
        document.addEventListener('DOMContentLoaded', () => {  
            const allNames = document.querySelectorAll('.doctor-names');  
            allNames.forEach(names => {  
                if (!names.classList.contains('hidden')) {  
                    names.classList.add('hidden');  
                }  
                names.classList.remove('show');  
            });  
        });  
    </script>  
